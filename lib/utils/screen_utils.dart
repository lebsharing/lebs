import 'package:flutter_screenutil/flutter_screenutil.dart';

class ScreenUtils {
  static double w(double width) {
    return width.w;
  }

  static double h(double height) {
    return height.h;
  }

  static double r(double value) {
    return value.r;
  }

  static double f(double fontSize) {
    return fontSize.sp;
  }
}
